    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(Id.getText().equals("")==false && fecha.getCalendar()!=null && Cliente.getText().equals("")==false && Valor.getText().equals("")==false ){
            MetodosGenerales general = new MetodosGenerales ();
            String validar=general.validarID("Cobros_prestamos.txt",Id.getText());
            SimpleDateFormat fomato_fecha = new SimpleDateFormat("MM/dd/YYYY");
            String fechaCuota = fomato_fecha.format(fecha.getCalendar().getTime());

            if(validar.equals("no")){
                if(cuotasCliente.getRowCount()==0){
                    JOptionPane.showMessageDialog(null,"La tabla de cuotas esta vacia, por favor llenarlo","Error",JOptionPane.ERROR_MESSAGE);
                    return;
                }
                
                general.registrar("Cobros_prestamos.txt",Id.getText()+"~"+fechaCuota+"~"+Cliente.getText()+"~"+Valor.getText()+"~"+"false");
                Double abono = Double.parseDouble(Valor.getText());
                 for(int i=0;i<cuotasCliente.getRowCount();i++){
                      String valorCuota = "";
                        Double ValorCuota = Double.parseDouble(cuotasCliente.getValueAt(i, 3).toString());
                        if(abono>=ValorCuota){
                            valorCuota = ValorCuota.toString();
                            abono-=ValorCuota;
                         int secuencia = i+1;
                        general.registrar("Detalles_Cobros.txt",Id.getText()+"~"+secuencia+"~"+cuotasCliente.getValueAt(i, 0).toString()+"~"+cuotasCliente.getValueAt(i, 1).toString()+"~"+valorCuota+"~"+"0"+"~"+"0"+"~"+"0");
                           
                        String validarCuota=general.verificarCuotaTrue(cuotasCliente.getValueAt(i, 0).toString(),cuotasCliente.getValueAt(i, 1).toString());
                            System.out.println(validarCuota);
                        if(validarCuota.equals("no")==false){
                            validarCuota = validarCuota.split("~")[0]+"~"+validarCuota.split("~")[1]+"~"+validarCuota.split("~")[2]+"~"+validarCuota.split("~")[3]+"~"+validarCuota.split("~")[4]+"~"+validarCuota.split("~")[5]+"~"+validarCuota.split("~")[6]+"~"+"false"; 
                            System.out.println(validarCuota);
                            general.modificar("Cuotas_Prestamos.txt", validarCuota.split("~")[0], validarCuota);
                        
                        }
                          if(abono<=0.0){
                            break;
                        }
                        }else{
                            JOptionPane.showMessageDialog(null,"Sobran: "+abono);
                             break;
                        }
                      
                       
                 }
                 
                 
                Id.setText("");
                fecha.setDate(null);
                Cliente.setText("");
                Valor.setText("");
                mensaje.setText("CREANDO");
                DefaultTableModel  modelo =(DefaultTableModel) cuotasCliente.getModel();
                modelo.setRowCount(0);
                cuotasCliente.setModel(modelo);

            }else{
                general.modificar("Detalles_Cobros.txt",Id.getText(),Id.getText()+"~"+fomato_fecha+"~"+Cliente.getText()+"~"+Valor.getText()+"~"+"false");
            }
        }else{
            JOptionPane.showMessageDialog(null,"Todos los campos son obligatorios, por favor llenarlo","Errro",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

