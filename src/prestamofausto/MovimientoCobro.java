    private void IdKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_IdKeyReleased
        MetodosGenerales usuario= new MetodosGenerales();
        String consulta=usuario.validarID("EncabezadoCuotas.txt", Id.getText());
        if(consulta.equals("no")){
            Date fechaHoy = new Date();
            fecha.setDate(fechaHoy);
            Cliente.setText("");
            Valor.setText("");
            mensaje.setText("CREANDO");
        }else{
            Date fecha_entrada = new Date(consulta.split("~")[1]);
            fecha.setDate(fecha_entrada);
            Cliente.setText(consulta.split("~")[2]);
            Valor.setText(consulta.split("~")[3]);
            mensaje.setText("Modificando");
        }
    }//GEN-LAST:event_IdKeyReleased

    private void ClienteKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_ClienteKeyReleased
        DefaultTableModel  modelo =(DefaultTableModel) cuotasCliente.getModel();
        modelo.setRowCount(0);
        cuotasCliente.setModel(modelo);
        if(Cliente.getText().equals("")){
            return;
        }

        MetodosGenerales general = new MetodosGenerales ();
        ArrayList<String> vector = new ArrayList();

        String dato_cliente = general.validarID("Clientes.txt",Cliente.getText());
        if(dato_cliente.equals("no")){
            JOptionPane.showMessageDialog(null,"Error el cliente no existe","Error",JOptionPane.ERROR_MESSAGE);
            return;

        }
//        else if(dato_cliente.split("~")[9].equals("true")==false || dato_cliente.split("~")[10].equals("Socio Activo")==false){
//            JOptionPane.showMessageDialog(null,"Error el cliente debe ser un socio activo y tener el status en true","Error",JOptionPane.ERROR_MESSAGE);
//            return;
//        }
        vector=general.LlenarTablaCondicion("Cuotas_Prestamos.txt",2,Cliente.getText());
        String datos [ ] = new String [5];
        if(vector.size()<1){
            JOptionPane.showMessageDialog(null,"Error el cliente no existe en el archivo cuotas prestamos","Error",JOptionPane.ERROR_MESSAGE);

        }
        
          SimpleDateFormat formato = new SimpleDateFormat("MM/dd/YYYY");
            String fechaD=formato.format(fecha.getCalendar().getTime());
            Date fechaActual = new Date(fechaD);
              
        for (int i = 0; i <vector.size(); i++) {
             Date fechaCuota = new Date(vector.get(i).split("~")[3]);
            if(fechaActual.after(fechaCuota)){
                datos[0]=vector.get(i).split("~")[0];
                datos[1]=vector.get(i).split("~")[1];
                datos[2]=vector.get(i).split("~")[3];
                datos[3]=vector.get(i).split("~")[4];
                datos[4]=vector.get(i).split("~")[7];
                modelo.addRow(datos);
            }

        }
    }//GEN-LAST:event_ClienteKeyReleased

